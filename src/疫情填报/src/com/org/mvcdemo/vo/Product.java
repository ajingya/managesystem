package com.org.mvcdemo.vo;

public class Product {
	private int pid;
	private String name;
	private String note;
	private String touch;
	private String infect;
	private String fanxiao;
	private String symptom;
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public String getTouch() {
		return touch;
	}
	public void setTouch(String touch) {
		this.touch = touch;
	}
	public String getInfect() {
		return infect;
	}
	public void setInfect(String infect) {
		this.infect = infect;
	}
	public String getFanxiao() {
		return fanxiao;
	}
	public void setFanxiao(String fanxiao) {
		this.fanxiao = fanxiao;
	}
	public String getSymptom() {
		return symptom;
	}
	public void setSymptom(String symptom) {
		this.symptom = symptom;
	}
	
	
}
